// The ternary operator in JavaScript is a concise way to write conditional statements.
//  It takes three operands: a condition followed by a question mark (?),
// An expression to execute if the condition is true,
// and another expression to execute if the condition is false.
// The syntax is as follows:
//condition ? expression_if_true : expression_if_false;

let num = 15;
let oddEven = num % 2 === 0 ? "Even" : "Odd";

console.log(oddEven);

//Ans Odd
